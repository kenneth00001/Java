var agora = new Date()
var dia = agora.getDay()

switch(dia) {
    case 0:
    console.log("Domingo")
    break
    case 1:
    console.log("Hoje é segunda-Feira")
    break
    case 2:
    console.log("Hoje é terça-Feira")
    break
    case 3:
    console.log("Hoje é Quarta-Feira")
    break
    case 4:
    console.log("Hoje é Quinta-Feira")
    break
    case 5:
    console.log("Hoje é Sexta-Feira")
    break
}